public class ResultsModel {
    private String grade;
    private String subjectName;
    private String status;
    private String maxMarks;
    private String marks;




    public String getResultGrade() {
        return grade;
    }

    public void setResultGrade(String grade) {
        this.grade = grade;
    }

    public String getResultSubjectName() {
        return subjectName;
    }

    public void setResultSubjectName(String subjectName) {
        this.subjectName = subjectName;
    }



    public String getResultStatus() {
        return status;
    }

    public void setResultStatus(String status) {
        this.status = status;
    }


    public String getResultmaxMarks() {
        return maxMarks;
    }

    public void setResultmaxMarks(String maxMarks) {
        this.maxMarks = maxMarks;
    }



    public String getResultMarks() {
        return marks;
    }

    public void setResultMarks(String marks) {
        this.marks = marks;
    }





}
